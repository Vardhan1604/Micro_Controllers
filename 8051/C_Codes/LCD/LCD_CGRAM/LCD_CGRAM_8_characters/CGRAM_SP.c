
#include "LCD.h"
#include "CGRAM_SP.h"

void LCD_CGRAM( unsigned int num , unsigned char loc)
{
	
	
	int i;
	
	
	char a[8]={0x04, 0x0e, 0x0e, 0x1f, 0x1f, 0x00, 0x04, 0x00};
	char b[8]={0x00, 0x04, 0x04, 0x15, 0x0e, 0X04, 0X00, 0x00};
	char c[8]={0x00, 0x04, 0x08, 0x1f, 0x08, 0x04, 0x00, 0x00};
	char d[8]={0x00, 0x04, 0x02, 0x1f, 0x02, 0x04, 0x00, 0x00};
	char e[8]={0x00, 0x04, 0x0E, 0x15, 0x04, 0x04, 0x00, 0x00};
	char f[8]={0x00, 0x0A, 0x1f, 0x1f, 0x0e, 0x04, 0x00, 0x00};
	char g[8]={0x0E, 0x1B, 0x11, 0x11, 0x11, 0x11, 0x1F, 0x00};
	char h[8]={0x0E, 0x1B, 0x11, 0x11, 0x1F, 0x1F, 0x1F, 0x00};
	


	
	for(i = 0; i < 8; i++)
	{
		LCD_cmd(0x40 + i);
		LCD_data(a[i]);
		LCD_cmd(0x48 + i);
		LCD_data(b[i]);
		LCD_cmd(0x50 + i);
		LCD_data(c[i]);
		LCD_cmd(0x58 + i);
		LCD_data(d[i]);
		LCD_cmd(0x60 + i);
		LCD_data(e[i]);
		LCD_cmd(0x68 + i);
		LCD_data(f[i]);
		LCD_cmd(0x70 + i);
		LCD_data(g[i]);
		LCD_cmd(0x78 + i);
		LCD_data(h[i]);
		
	}
	
		LCD_cmd(loc);
		LCD_data(num);
	
	
	
}